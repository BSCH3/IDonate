            Charles Babbage was born in London on December 26, 1791, the son of Benjamin Babbage, a London banker. As a youth 
            Babbage was his own instructor in algebra, of which he was passionately fond, and was well read in the continental 
            mathematics of his day. Upon entering Trinity College, Cambridge, in 1811, he found himself far in advance of his 
            tutors in mathematics. Babbage co-founded the Analytical Society for promoting continental mathematics and reforming 
            the mathematics of Newton then taught at the university. 
            In his twenties Babbage worked as a mathematician, principally in the calculus of functions. He was elected a Fellow 
            of the Royal Society in 1816 and played a prominent part in the foundation of the Astronomical Society (later Royal 
            Astronomical Society) in 1820. It was about this time that Babbage first acquired the interest in calculating 
            machinery that became his consuming passion for the remainder of his life.
            The first computers used vacuum tubes for circuitry and magnetic drums for memory, and were often enormous, taking 
