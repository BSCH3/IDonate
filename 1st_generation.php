            Joseph-Marie Jacquard was not the inventor of the programmable loom, as many people imagine, actually he created an 
            attachment to the loom, which played a very important role not only in the textile industry, but also in development 
            of other programmable machines, such as computers, for example the Analytical Engine of Charles Babbage.

            Joseph-Marie Charles (known as Jacquard after the nickname of his family) was born on 7th of July, 1752, in Lyon, 
            France. He was the fifth of nine children of Jean Charles (nicknamed Jacquard) (1724-1772), a master weaver of 
            brocaded fabrics, and his wife, Antoinette Rive. From the nine children, only Joseph and his sister Clémence 
            (born 1747) survived to adulthood. Like the sons of many Lyons weavers, Joseph did not go to school, because his 
            father needed him to perform odd jobs in the workshop. He learned to read as a thirteen-year-old boy by his 
            brother-in-law Barret, a cultured man.

            One of the first improvements of Jacquard was to eliminate the paper strip from Vaucanson s mechanism and to return 
            to Falcons chain of punched cards. Then, he tried to avoid the expensive metal cylinders of Vaucanson. In fact, the.
            term Jacquard loom is a misnomer, actually Jacquard s invented an attachment (head), that adapts to a great many type 
            of looms, that allow the weaving machine to create the intricate patterns often seen in Jacquard weaving. Thus any 
            loom that uses the attachment is called a Jacquard loom.

            Each position in the punched card of the loom corresponds to a hook, which can either be raised or stopped dependant 
            on whether the hole is punched out of the card or the card is solid. The hook raises or lowers the harness, which 
            carries and guides the warp thread so that the weft will either lie above or below it. The sequence of raised and 
            lowered threads is what creates the pattern. Each hook can be connected via the harness to a number of 
            threads, allowing more than one repeat of a pattern. For example, a loom with a 500-hook head might have 
            four threads connected to each hook, resulting in a fabric that is 2000 warp ends wide with four repeats of 
            the weave going across. 

