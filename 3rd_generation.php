        <p>  
            The development of the integrated circuit was the hallmark of the third generation of computers. 
            Transistors were miniaturized and placed on silicon chips, called semiconductors, 
            which drastically increased the speed and efficiency of computers.
            Instead of punched cards and printouts, users interacted with third generation computers through keyboards and 
            monitors and interfaced with an operating system, which allowed the device to run many different applications at one 
            time with a central program that monitored the memory. 
            Computers for the first time became accessible to a mass audience because they were smaller and cheaper than their 
            predecessors.</p>
        <p>
            In this era, there were several innovations in various fields of computer technology. These include Integrated 
            Circuits (ICs), Semiconductor Memories, Microprogramming, various patterns of parallel processing and introduction of 
            Operating Systems and time-sharing. 
            In the Integrated Circuit, division there was gradual progress. Firstly, there were small-scale integration (SSI) 
            circuits (having 10 devices per chip), which evolved to medium scale integrated (MSI) circuits (having 100 devices 
            per chip). There were also developments of multi-layered printed circuits.
        </p>
        <p>
            Jack Kilby, an engineer with Texas Instruments, developed the integrated circuit (IC) in 1958 wich 
            defined the third generation computers. The IC combined three electronic components onto a small 
            silicon disc, which was made from quartz. Scientists later managed to fit even more components on a
            single chip, called a semiconductor. As a result, computers became ever smaller as more components 
            were squeezed onto the chip. Another third-generation development included the use of an operating 
            system that allowed machines to run many different programs at once with a central program that 
            monitored and coordinated the computer s memory.  
        </p>

