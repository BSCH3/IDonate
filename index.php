        <p>
            The history of computer development is often referred to in reference to the different 
            generations of computing devices. 
            Generation in computer terminology is a change in technology a computer is/was
            being used. Initially, the generation term was used to distinguish between varying
            hardware technologies. But nowadays, generation includes both hardware and
            software, which together make up an entire computer system.</p>
            <p>Each generation of computer is characterized by a major technological development that fundamentally 
            changed the way computers operate, resulting in increasingly smaller, cheaper, 
            and more powerful and more efficient and reliable devices.           
            There are totally five computer generations known till date. Each generation has been
            discussed in detail along with their time period and characteristics. Here approximate
            dates against each generations have been mentioned which are normally accepted.</p>
            <p>From the earliest times the need to carry out calculations has been developing. The first steps
            involved the development of counting and calculation aids such as the counting board and the
            abacus.</p>
          <p><strong>Pascal </strong>(1623-62) was the son of a tax collector and a mathematical genius. He designed the first
            mechanical calculator (Pascaline) based on gears. It performed addition and subtraction.</p>
            <p><strong>Leibnitz</strong> (1646-1716) was a German mathematician and built the first calculator to do
            multiplication and division. It was not reliable due to accuracy of contemporary parts.</p>
             <img class="featurette-image1 img-responsive" src="Images/1stgen.jpg" style="width: 350px; height: 250px;">
            <p><strong>Babbage</strong> (1792-1872) was a British inventor who designed an ‘analytical engine’ incorporating
            the ideas of a memory and card input/ouput for data and instructions. Again the current
            technology did not permit the complete construction of the machine.Babbage is largely remembered because of the 
            work of Augusta Ada (Countess of Lovelace) who was probably the first computer programmer.</p>
            <p><strong>Burroughs</strong> (1855-98) introduced the first commercially successful mechanical adding machine of
            which a million were sold.by 1926.</p>
            <p><strong>Hollerith</strong> developed an electromechanical punched-card tabulator to tabulate the data for 1890
            U.S. census. Data was entered on punched cards and could be sorted according to the census
            requirements. The machine was powered by electricity. He formed the Tabulating Machine
            Company which became International Business Machines (IBM). IBM is still one of the
            largest computer companies in the world.</p>
            <p><strong>Aiken</strong> (1900-73) a Harvard professor with the backing of IBM built the Harvard Mark I
            computer (51ft long) in 1944. It was based on relays (operate in milliseconds) as opposed to the
            use of gears. It required 3 seconds for a multiplication.</p>
            <p><strong>Eckert and Mauchly</strong> designed and built the ENIAC in 1946 for military computations. It used
            vacuum tubes (valves) which were completely electronic (operated in microseconds) as opposed
            to the relay which was electromechanical.         
            It weighed 30 tons, used 18000 valves, and required 140 kwatts of power. It was 1000 times
            faster than the Mark I multiplying in 3 milliseconds. ENIAC was a decimal machine and could
            not be programmed without altering its setup manually.</p>           
            <p><strong>Atanasoff</strong> had built a specialised computer in 1941 and was visited by Mauchly before the
                <img class="featurette-image img-responsive" src="Images/turing.jpg" style="width: 350px; height: 250px;">
            construction of the ENIAC. He sued Mauchly in a case which was decided in his favour in 1974!</p>
            
           <p><strong>Von Neumann</strong> was a scientific genius and was a consultant on the ENIAC project. He
            formulated plans with Mauchly and Eckert for a new computer (EDVAC) which was to store
            programs as well as data.
            This is called the stored program concept and Von Neumann is credited with it. Almost all
            modern computers are based on this idea and are referred to as von neumann machines.
            He also concluded that the binary system was more suitable for computers since switches have
            only two values. He went on to design his own computer at Princeton which was a general
            purpose machine.</p>
            <p><strong>Alan Turing</strong> was a British mathematician who also made significant contributions to the early
            development of computing, especially to the theory of computation. He developed an abstract
            theoretical model of a computer called a Turing machine which is used to capture the notion of
            computable i.e. what problems can and what problems cannot be computed. Not all problems
            can be solved on a computer.
            Note: A Turing machine is an abstract model and not a physical computer.
            From the 1950’s, the computer age took off in full force. The years since then have been divided
            into periods or generations based on the technology used.</p>

